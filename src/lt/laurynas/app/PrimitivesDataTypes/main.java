package lt.laurynas.app.PrimitivesDataTypes;

public class main {
    public static void main(String[] args) {
        System.out.println("Minimali int reikšmė yra: " + Integer.MIN_VALUE);
        System.out.println("Maksimali int reikšmė yra: " + Integer.MAX_VALUE);
        System.out.println("UnderFlow effect: "+(Integer.MIN_VALUE-1));
        System.out.println("OverFlow effect: "+(Integer.MAX_VALUE+1));
    }
}
